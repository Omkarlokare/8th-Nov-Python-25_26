# String type variable
box = "books, ball"
print(box)
print(type(box))

# Integer type variable

num = 12
print(num)
print(type(num))

# float type variable

num1 = 12.1
print(num1)
print(type(num1))


# Boolean data type

a = 12
b = 13
num1 = a == b
num2 = a > b
num3 = a < b

print(num1)
print(type(num1))

print(num2)
print(type(num2))

print(num3)
print(type(num3))
